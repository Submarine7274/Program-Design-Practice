#include <stdio.h>
int main (void){
    int i;  //宣告整數變數i
    float f;    //宣告浮點數變數f
    scanf("%d %f", &i, &f); //讀取整數與浮點數 兩數之間用空白隔開
    printf("|%d|%5d|%-5d|%5.3d|\n", i, i, i, i);    //輸出整數 %d是直接輸出整數 %5d是輸出整數且寬度為5 % -5d是輸出整數且寬度為5靠左對齊 %5.3d是輸出整數且寬度為5不足3位補0
    printf("|%10.3f|%10.3e|%-10g|", f, f, f);   //輸出浮點數 %10.3f是輸出浮點數且寬度為10小數點後3位 %10.3e是輸出科學記號且寬度為10小數點後3位 %-10g是輸出浮點數且寬度為10靠左對齊
    return 0;   //程式結束
}